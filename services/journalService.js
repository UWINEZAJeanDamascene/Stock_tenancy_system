  /**
   * Payroll payment
   * Debit: Payroll Expense
   * Credit: Cash/Bank (net pay)
   * Credit: Tax Payable (withheld tax)
   * Credit: Employee Deductions Payable
   */
  static async createPayrollEntry(companyId, userId, payroll) {
    const lines = [];

    const grossPay = payroll.grossPay || 0;
    const netPay = payroll.netPay || 0;
    const taxWithheld = payroll.taxWithheld || 0;
    const ssnWithheld = payroll.ssnWithheld || 0;
    const otherDeductions = payroll.otherDeductions || 0;

    // Determine cash account
    const cashAccount = payroll.paymentMethod === 'bank'
      ? DEFAULT_ACCOUNTS.cashAtBank
      : DEFAULT_ACCOUNTS.cashInHand;

    // Debit: Payroll Expense
    lines.push(this.createDebitLine(
      DEFAULT_ACCOUNTS.payrollExpenses || '5410',
      grossPay,
      `Payroll: ${payroll.period || 'Salary'}`
    ));

    // Credit: Cash/Bank (net pay to employee)
    if (netPay > 0) {
      lines.push(this.createCreditLine(
        cashAccount,
        netPay,
        `Net salary payment: ${payroll.period || 'Salary'}`
      ));
    }

    // Credit: Tax Payable (withheld tax)
    if (taxWithheld > 0) {
      lines.push(this.createCreditLine(
        DEFAULT_ACCOUNTS.taxPayable || '2310',
        taxWithheld,
        `Tax withheld: ${payroll.period || 'Salary'}`
      ));
    }

    // Credit: RSSB Payable (social security)
    if (ssnWithheld > 0) {
      lines.push(this.createCreditLine(
        DEFAULT_ACCOUNTS.rssbPayable || '2320',
        ssnWithheld,
        `RSSB contribution: ${payroll.period || 'Salary'}`
      ));
    }

    // Credit: Employer Contribution Payable
    const employerContribution = payroll.employerContribution || 0;
    if (employerContribution > 0) {
      lines.push(this.createCreditLine(
        DEFAULT_ACCOUNTS.employerContributionPayable || '2310',
        employerContribution,
        `Employer contribution: ${payroll.period || 'Salary'}`
      ));
    }

    return this.createEntry(companyId, userId, {
      date: payroll.paymentDate || new Date(),
      description: `Payroll: ${payroll.period || 'Salary'} - ${payroll.employeeName}`,
      sourceType: 'payroll',
      sourceId: payroll._id,
      lines,
      isAutoGenerated: true
    });
  }

  // =====================================================
  // TAX PAYMENT
  // =====================================================

  /**
   * Tax payment
   * Debit: Tax Payable
   * Credit: Cash/Bank
   */
  static async createTaxPaymentEntry(companyId, userId, taxPayment) {
    const lines = [];
    const amount = taxPayment.amount || 0;

    // Determine cash account
    const cashAccount = taxPayment.paymentMethod === 'bank'
      ? DEFAULT_ACCOUNTS.cashAtBank
      : DEFAULT_ACCOUNTS.cashInHand;

    // Debit: Tax Payable
    lines.push(this.createDebitLine(
      DEFAULT_ACCOUNTS.taxPayable || '2310',
      amount,
      `Tax payment: ${taxPayment.taxType || 'VAT'}`
    ));

    // Credit: Cash/Bank
    lines.push(this.createCreditLine(
      cashAccount,
      amount,
      `Tax payment: ${taxPayment.taxType || 'VAT'}`
    ));

    return this.createEntry(companyId, userId, {
      date: taxPayment.paymentDate || new Date(),
      description: `Tax payment: ${taxPayment.taxType || 'VAT'}`,
      sourceType: 'tax_payment',
      sourceId: taxPayment._id,
      lines,
      isAutoGenerated: true
    });
  }
