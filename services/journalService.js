  /**
   * Loan payment (principal + interest)
   * Debit: Loan Liability (principal)
   * Debit: Interest Expense
   * Credit: Cash/Bank
   */
  static async createLoanPaymentEntry(companyId, userId, payment) {
    const lines = [];

    const cashAccount = payment.paymentMethod === 'bank' 
      ? DEFAULT_ACCOUNTS.cashAtBank 
      : DEFAULT_ACCOUNTS.cashInHand;

    const principal = payment.principalAmount || 0;
    const interest = payment.interestAmount || 0;
    const total = principal + interest;

    // Debit: Loan Liability (principal)
    if (principal > 0) {
      lines.push(this.createDebitLine(
        DEFAULT_ACCOUNTS.shortTermLoans,
        principal,
        `Loan payment: ${payment.loanNumber}`
      ));
    }

    // Debit: Interest Expense
    if (interest > 0) {
      lines.push(this.createDebitLine(
        DEFAULT_ACCOUNTS.interestExpense,
        interest,
        `Interest payment: ${payment.loanNumber}`
      ));
    }

    // Credit: Cash/Bank
    lines.push(this.createCreditLine(
      cashAccount,
      total,
      `Loan payment: ${payment.loanNumber}`
    ));

    return this.createEntry(companyId, userId, {
      date: payment.date || new Date(),
      description: `Loan payment: ${payment.loanNumber}`,
      sourceType: 'loan',
      lines,
      isAutoGenerated: true
    });
  }

